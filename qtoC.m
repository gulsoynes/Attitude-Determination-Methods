function [C] = qtoC(q)
%Function to obtain orientation matrix in terms of quaternion
q1 = q(1); q2 = q(2); q3 = q(3); q0 = q(4); %Scalar part of quaternion

C = ...
    [
    q1^2 - q2^2 - q3^2 + q0^2 , ...
    2 * ( q1*q2 + q3*q0 ) ,...
    2 * ( q1*q3 - q2*q0 ); ...
    ...
    2 * ( q1 * q2 - q3 * q0 ) , ...
    -q1^2 + q2^2 - q3^2 + q0^2 , ...
    2 * ( q2*q3 + q1*q0 ); ...
    ...
    2 * ( q1 * q3 + q2 * q0 ) , ...
    2 * ( q2*q3 - q1*q0 ) , ...
    -q1^2 - q2^2 + q3^2 + q0^2
    ] ;
end